    private void registrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_registrarActionPerformed
            Connection con = Conexion.ConnectDB();
            if (txtdeposito.getText().equals("")) {
                JOptionPane.showMessageDialog( this, "Ingrese Cantidad a Depositar","Error", JOptionPane.ERROR_MESSAGE);
                txtdeposito.requestFocus();
                return;
            }
            try{
            numeros();
            Statement stmt;
            stmt= con.createStatement();
            String sql1="Select paciente from depositos where paciente= '" + idpaciente + "' and estado=1";
            rs=stmt.executeQuery(sql1);
            if(rs.next()){
                JOptionPane.showMessageDialog( this, "El Paciente ya esta Ingresado","Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
            //QUEry insert datos a tabla depositos
            String sql= "insert into depositos("
                    + "paciente,"
                    + "valor,"
                    + "fecha,"
                    + "saldo_disponible,"
                    + "estado) values ('"
                    +idpaciente+"','" 
                    + txtdeposito.getText() +"','" 
                    +fecha+"','" 
                    + txtdeposito.getText() +"','" 
                    +1+ "')";

            pst=con.prepareStatement(sql);
            pst.execute();
            JOptionPane.showMessageDialog(this,"Registrado con √©xito","Depositos",JOptionPane.INFORMATION_MESSAGE);
            FadeEffect.fadeOut(this, 50, 0.1f);
            this.dispose();
        } catch (SQLException ex) {
        Logger.getLogger(modal_Deposito.class.getName()).log(Level.SEVERE, null, ex);
    }
    }//GEN-LAST:event_registrarActionPerformed

